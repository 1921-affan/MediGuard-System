'use client';
import { useEffect, useState } from 'react';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Calendar, FileText, User, Printer } from 'lucide-react';
import { Button } from '@/components/ui/button';

export default function MedicalHistoryPage() {
    const [history, setHistory] = useState<any[]>([]);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        fetchHistory();
    }, []);

    const fetchHistory = async () => {
        const token = localStorage.getItem('token');
        try {
            // 1. Get Patient Profile to get Patient_ID
            const profileRes = await fetch('http://localhost:5000/api/auth/profile', {
                headers: { 'Authorization': `Bearer ${token}` }
            });
            if (!profileRes.ok) return;
            const profile = await profileRes.json();
            const patientId = profile.Patient_ID;

            // 2. Get History
            const res = await fetch(`http://localhost:5000/api/clinical/${patientId}`, {
                headers: { 'Authorization': `Bearer ${token}` }
            });
            if (res.ok) setHistory(await res.json());

        } catch (err) {
            console.error(err);
        } finally {
            setLoading(false);
        }
    };

    const handlePrint = (record: any) => {
        const printWindow = window.open('', '_blank');
        if (!printWindow) return;

        const html = `
            <html>
            <head>
                <title>Medical Prescription - ${record.Record_ID}</title>
                <style>
                    body { font-family: 'Helvetica', sans-serif; padding: 40px; }
                    .header { text-align: center; border-bottom: 2px solid #333; padding-bottom: 20px; mb-8; }
                    .header h1 { margin: 0; color: #1a365d; }
                    .meta { margin-bottom: 30px; }
                    .grid { display: grid; grid-template-columns: 1fr 1fr; gap: 20px; }
                    .section { margin-bottom: 20px; }
                    .label { font-weight: bold; color: #666; font-size: 0.9em; }
                    .value { font-size: 1.1em; margin-top: 4px; }
                    .meds { width: 100%; border-collapse: collapse; margin-top: 20px; }
                    .meds th, .meds td { border: 1px solid #ddd; padding: 10px; text-align: left; }
                    .meds th { background: #f8f9fa; }
                    .footer { margin-top: 50px; text-align: center; font-size: 0.8em; color: #999; }
                </style>
            </head>
            <body>
                <div class="header">
                    <h1>MediGuard Medical Record</h1>
                    <p>Official Digital Prescription</p>
                </div>

                <div class="grid meta">
                    <div>
                        <div class="section">
                            <div class="label">DOCTOR</div>
                            <div class="value">Dr. ${record.Doctor_Name || record.Doctor_ID}</div>
                        </div>
                        <div class="section">
                            <div class="label">DATE</div>
                            <div class="value">${new Date(record.Visit_Date).toLocaleDateString()}</div>
                        </div>
                    </div>
                </div>

                <div class="section">
                    <div class="label">DIAGNOSIS</div>
                    <div class="value">${record.Diagnosis_ICD10}</div>
                </div>

                <div class="section">
                    <div class="label">SYMPTOMS</div>
                    <div class="value">${record.Symptoms_Text}</div>
                </div>

                <div class="section">
                    <div class="label">REMARKS</div>
                    <div class="value">${record.Doctor_Remarks}</div>
                </div>

                ${record.Follow_Up_Date ? `
                <div class="section" style="color: #c53030;">
                    <div class="label">FOLLOW UP REQUIRED</div>
                    <div class="value">${new Date(record.Follow_Up_Date).toLocaleDateString()}</div>
                </div>` : ''}

                <div class="footer">
                    Generated by MediGuard System â€¢ ${new Date().toLocaleString()}
                </div>
                
                <script>
                    window.onload = function() { window.print(); }
                </script>
            </body>
            </html>
        `;

        printWindow.document.write(html);
        printWindow.document.close();
    };

    if (loading) return <div className="p-8">Loading history...</div>;

    return (
        <div className="space-y-6">
            <h1 className="text-3xl font-bold">Medical History</h1>

            <div className="space-y-4">
                {history.length > 0 ? (
                    history.map((record) => (
                        <Card key={record.Record_ID}>
                            <CardHeader className="pb-2">
                                <div className="flex justify-between items-start">
                                    <div>
                                        <CardTitle className="text-lg text-primary">{record.Diagnosis_ICD10}</CardTitle>
                                        <div className="flex items-center gap-2 text-sm text-gray-500 mt-1">
                                            <Calendar className="h-4 w-4" />
                                            {new Date(record.Visit_Date).toLocaleDateString()}
                                        </div>
                                    </div>
                                    <div className="flex flex-col items-end gap-2">
                                        <div className="flex items-center gap-1 text-sm bg-blue-50 text-blue-700 px-2 py-1 rounded">
                                            <User className="h-4 w-4" />
                                            Dr. {record.Doctor_Name || record.Doctor_ID}
                                        </div>
                                        <Button size="sm" variant="outline" onClick={() => handlePrint(record)} className="h-8 text-xs gap-1">
                                            <Printer className="h-3 w-3" /> Print PDF
                                        </Button>
                                    </div>
                                </div>
                            </CardHeader>
                            <CardContent>
                                <div className="grid gap-4 md:grid-cols-2">
                                    <div className="bg-gray-50 p-3 rounded">
                                        <span className="font-semibold text-sm">Symptoms:</span>
                                        <p className="text-sm mt-1">{record.Symptoms_Text}</p>
                                    </div>
                                    <div className="bg-gray-50 p-3 rounded">
                                        <span className="font-semibold text-sm">Doctor's Remarks:</span>
                                        <p className="text-sm mt-1">{record.Doctor_Remarks}</p>
                                    </div>
                                </div>
                                {record.Follow_Up_Date && (
                                    <div className="mt-4 text-sm text-orange-600 font-medium">
                                        Follow Up Required: {new Date(record.Follow_Up_Date).toLocaleDateString()}
                                    </div>
                                )}
                            </CardContent>
                        </Card>
                    ))
                ) : (
                    <div className="text-center py-12 text-gray-400 border-2 border-dashed rounded-lg">
                        <FileText className="h-10 w-10 mx-auto mb-2 opacity-50" />
                        <p>No medical records found.</p>
                    </div>
                )}
            </div>
        </div>
    );
}
